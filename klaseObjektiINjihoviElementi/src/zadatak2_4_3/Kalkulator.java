package zadatak2_4_3;

public class Kalkulator {

	int saberi(int prviSabirak, int drugiSabirak) {
		return prviSabirak + drugiSabirak;
	}
	
	int oduzmi(int umanjenik, int umanjilac) {
		return umanjenik - umanjilac;
	}	
	
	int pomnozi(int prviCinilac, int drugiCinilac) {
		return prviCinilac * drugiCinilac;
	}
	
	double pomnozi(double prviCinilac, double drugiCinilac) {
		return prviCinilac * drugiCinilac;
	}
	
	double podeli(double deljenik, double delilac) {
		return deljenik / delilac;
	}	

	int podeliDIV(int deljenik, int delilac) {
		return deljenik / delilac;
	}
	
	int podeliMOD(int deljenik, int delilac) {
		return deljenik % delilac;
	}	
	
}
